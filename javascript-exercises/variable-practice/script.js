// Example: Creating variables with different keywords
const myName = "Your Name";  // const for values that won't change
let myAge = 25;              // let for values that might change
let favoriteColor = "blue";

// Display variables in the console
console.log("=== Variable Practice ===");
console.log("Name:", myName);
console.log("Age:", myAge);
console.log("Favorite Color:", favoriteColor);

// You can also use template literals (backticks) for easier string formatting
console.log(`Hello, my name is ${myName} and I am ${myAge} years old.`);

// Try modifying a variable
myAge = 26;
console.log("Updated age:", myAge);

